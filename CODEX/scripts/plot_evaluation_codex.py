import subprocess
import sys
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.ticker as mtick
import numpy as np
import pandas as pd
import seaborn as sns
import yaml

sns.set_style("whitegrid")
plt.rcParams.update({"figure.dpi": 150, "savefig.dpi": 300})

ROOT = Path(__file__).resolve().parents[2]
CODEX_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(CODEX_ROOT))

from replication_analyzer_codex.annotations import load_annotations_for_codex
from replication_analyzer_codex.weak_labels import build_annotation_dict, build_weak_labels_for_read

CLASS_NAMES = ["background", "left_fork", "right_fork", "origin"]
CLASS_COLORS = ["#888888", "#2196F3", "#FF5722", "#4CAF50"]


def _add_true_labels(predictions: pd.DataFrame, config: dict) -> pd.DataFrame:
    """Join predictions with weak ground-truth labels derived from annotation BEDs."""
    left_forks, right_forks, origins, terminations = load_annotations_for_codex(config)
    annotation_dict = build_annotation_dict(left_forks, right_forks, origins, terminations)
    margin_bp = config.get("labeling", {}).get("trusted_negative_margin_bp", 1000)

    true_class_list = []
    supervised_list = []

    for read_id, group in predictions.groupby("read_id", sort=False):
        read_df = group[["chr", "start", "end", "read_id"]].copy().reset_index(drop=True)
        _, sample_weight, stats = build_weak_labels_for_read(
            read_df=read_df,
            annotation_dict=annotation_dict,
            background_margin_bp=margin_bp,
        )
        true_class_list.extend(stats["y_safe"].tolist())
        supervised_list.extend(sample_weight.tolist())

    predictions = predictions.copy()
    predictions["true_class"] = true_class_list
    predictions["supervised"] = supervised_list
    return predictions


# ──────────────────────────────────────────────────────────────────────────────
# 1. Training history
# ──────────────────────────────────────────────────────────────────────────────

def plot_training_history(history_path: Path, output_dir: Path, best_epoch: int | None = None):
    df = pd.read_csv(history_path, sep="\t")
    epochs = np.arange(1, len(df) + 1)

    has_per_class = "val_masked_precision_left_fork" in df.columns

    nrows = 3 if has_per_class else 2
    fig, axes = plt.subplots(nrows, 2, figsize=(14, 5 * nrows))
    fig.suptitle("Training History — CODEX weak-supervision model", fontsize=14, fontweight="bold")

    def _vline(ax):
        if best_epoch is not None:
            ax.axvline(best_epoch, linestyle="--", color="grey", alpha=0.7, label=f"Best (ep {best_epoch})")

    # Loss
    ax = axes[0, 0]
    ax.plot(epochs, df["loss"], label="Train", linewidth=2, color="#1565C0")
    ax.plot(epochs, df["val_loss"], label="Val", linewidth=2, color="#C62828")
    _vline(ax)
    ax.set_xlabel("Epoch"); ax.set_ylabel("Loss"); ax.set_title("Loss (focal)"); ax.legend(); ax.grid(True, alpha=0.3)

    # Masked accuracy
    ax = axes[0, 1]
    ax.plot(epochs, df["masked_accuracy"], label="Train", linewidth=2, color="#1565C0")
    ax.plot(epochs, df["val_masked_accuracy"], label="Val", linewidth=2, color="#C62828")
    _vline(ax)
    ax.set_xlabel("Epoch"); ax.set_ylabel("Accuracy"); ax.set_title("Masked accuracy"); ax.legend(); ax.grid(True, alpha=0.3)

    # Macro F1
    ax = axes[1, 0]
    ax.plot(epochs, df["masked_f1_macro"], label="Train", linewidth=2, color="#6A1B9A")
    ax.plot(epochs, df["val_masked_f1_macro"], label="Val", linewidth=2, color="#AD1457")
    _vline(ax)
    ax.set_xlabel("Epoch"); ax.set_ylabel("Macro F1"); ax.set_title("Masked macro-F1 ⭐"); ax.legend(); ax.grid(True, alpha=0.3)

    # Learning rate
    ax = axes[1, 1]
    ax.plot(epochs, df["learning_rate"], linewidth=2, color="#00695C", marker="o", markersize=3)
    ax.set_xlabel("Epoch"); ax.set_ylabel("LR"); ax.set_title("Learning rate schedule")
    ax.set_yscale("log"); ax.grid(True, alpha=0.3)
    lr_vals = df["learning_rate"].values
    for i in range(1, len(lr_vals)):
        if lr_vals[i] < lr_vals[i - 1] * 0.9:
            ax.axvline(i + 1, linestyle=":", color="orange", alpha=0.8)

    # Per-class Precision / Recall (only if tracked)
    if has_per_class:
        class_styles = {
            "left_fork":  ("#2196F3", "#1565C0"),   # blues
            "right_fork": ("#FF5722", "#BF360C"),   # oranges
            "origin":     ("#4CAF50", "#1B5E20"),   # greens
        }

        ax = axes[2, 0]
        ax.set_title("Val Precision per class"); ax.set_xlabel("Epoch"); ax.set_ylabel("Precision")
        for cls, (light, dark) in class_styles.items():
            col = f"val_masked_precision_{cls}"
            if col in df.columns:
                ax.plot(epochs, df[col], label=cls.replace("_", " "), linewidth=2, color=dark)
        _vline(ax); ax.set_ylim(0, 1.05); ax.legend(); ax.grid(True, alpha=0.3)

        ax = axes[2, 1]
        ax.set_title("Val Recall per class"); ax.set_xlabel("Epoch"); ax.set_ylabel("Recall")
        for cls, (light, dark) in class_styles.items():
            col = f"val_masked_recall_{cls}"
            if col in df.columns:
                ax.plot(epochs, df[col], label=cls.replace("_", " "), linewidth=2, color=dark,
                        linestyle="--")
        _vline(ax); ax.set_ylim(0, 1.05); ax.legend(); ax.grid(True, alpha=0.3)

    plt.tight_layout()
    out = output_dir / "training_history.png"
    plt.savefig(out, bbox_inches="tight")
    plt.close()
    print(f"✅ Training history saved: {out}")


# ──────────────────────────────────────────────────────────────────────────────
# 2. Window-based confusion matrix + per-class metrics
# ──────────────────────────────────────────────────────────────────────────────

def plot_confusion_matrix(predictions_path: Path, output_dir: Path, config: dict | None = None):
    """
    Window-based confusion matrix.

    Derives ground-truth labels from the annotation BEDs via _add_true_labels()
    and evaluates only on supervised windows (supervised > 0).

    predictions.tsv must have columns: read_id, chr, start, end, predicted_class.
    """
    preds = pd.read_csv(predictions_path, sep="\t")

    # Add ground-truth labels from annotation BEDs
    if "true_class" not in preds.columns:
        if config is None:
            print("⚠️  No config provided and no true_class column — window confusion matrix skipped.")
            return
        print("   Deriving ground-truth labels from annotation BEDs …")
        preds = _add_true_labels(preds, config)

    # Only evaluate supervised windows
    if "supervised" in preds.columns:
        mask = preds["supervised"].values > 0
    elif "sample_weight" in preds.columns:
        mask = preds["sample_weight"].values > 0
    else:
        mask = np.ones(len(preds), dtype=bool)

    y_true = preds["true_class"].values[mask]
    y_pred = preds["predicted_class"].values[mask]

    n_classes = 4
    labels = list(range(n_classes))

    # Confusion matrix
    from sklearn.metrics import confusion_matrix, classification_report
    cm = confusion_matrix(y_true, y_pred, labels=labels)
    cm_norm = cm.astype(float) / cm.sum(axis=1, keepdims=True).clip(min=1)

    fig, axes = plt.subplots(1, 3, figsize=(20, 6))
    fig.suptitle("Window-based classification — val set", fontsize=13, fontweight="bold")

    # Raw counts
    sns.heatmap(cm, annot=True, fmt="d", cmap="Blues",
                xticklabels=CLASS_NAMES, yticklabels=CLASS_NAMES, ax=axes[0])
    axes[0].set_xlabel("Predicted"); axes[0].set_ylabel("True"); axes[0].set_title("Confusion matrix (counts)")

    # Normalised
    sns.heatmap(cm_norm, annot=True, fmt=".2f", cmap="Blues",
                xticklabels=CLASS_NAMES, yticklabels=CLASS_NAMES, ax=axes[1])
    axes[1].set_xlabel("Predicted"); axes[1].set_ylabel("True"); axes[1].set_title("Confusion matrix (row-normalised)")

    # Per-class bar chart: Precision / Recall / F1
    report = classification_report(y_true, y_pred, labels=labels, target_names=CLASS_NAMES, output_dict=True, zero_division=0)
    metrics = {cls: (report[cls]["precision"], report[cls]["recall"], report[cls]["f1-score"])
               for cls in CLASS_NAMES}

    x = np.arange(len(CLASS_NAMES))
    w = 0.25
    ax = axes[2]
    ax.bar(x - w, [metrics[c][0] for c in CLASS_NAMES], w, label="Precision", color="#1976D2")
    ax.bar(x,     [metrics[c][1] for c in CLASS_NAMES], w, label="Recall",    color="#388E3C")
    ax.bar(x + w, [metrics[c][2] for c in CLASS_NAMES], w, label="F1",        color="#7B1FA2")
    ax.set_xticks(x); ax.set_xticklabels(CLASS_NAMES, rotation=15)
    ax.set_ylim(0, 1.05); ax.set_ylabel("Score"); ax.set_title("Per-class P / R / F1")
    ax.legend(); ax.grid(True, alpha=0.3, axis="y")

    # Print classification report
    print("\n📊 Window-based classification report (val, supervised windows only):")
    print(classification_report(y_true, y_pred, labels=labels, target_names=CLASS_NAMES, zero_division=0))

    plt.tight_layout()
    out = output_dir / "confusion_matrix_window_based.png"
    plt.savefig(out, bbox_inches="tight")
    plt.close()
    print(f"✅ Confusion matrix saved: {out}")


# ──────────────────────────────────────────────────────────────────────────────
# 3. Feature-based IoU heatmaps
# ──────────────────────────────────────────────────────────────────────────────

def plot_iou_heatmaps(sweep_path: Path, output_dir: Path):
    sweep = pd.read_csv(sweep_path, sep="\t")
    event_types = sweep["event_type"].unique()

    for metric in ("precision", "recall", "f1"):
        n_events = len(event_types)
        fig, axes = plt.subplots(1, n_events, figsize=(6 * n_events, 5))
        if n_events == 1:
            axes = [axes]
        fig.suptitle(f"Feature-based {metric.capitalize()} — val set", fontsize=13, fontweight="bold")

        for ax, event in zip(axes, sorted(event_types)):
            sub = sweep[sweep["event_type"] == event]
            pivot = sub.pivot_table(index="prob_threshold", columns="iou_threshold", values=metric)
            pivot = pivot.sort_index(ascending=False)
            sns.heatmap(pivot, annot=True, fmt=".2f", cmap="YlOrRd", vmin=0, vmax=1,
                        ax=ax, cbar_kws={"shrink": 0.8})
            ax.set_title(f"{event}")
            ax.set_xlabel("IoU threshold")
            ax.set_ylabel("Prob threshold")

        plt.tight_layout()
        out = output_dir / f"iou_heatmap_{metric}.png"
        plt.savefig(out, bbox_inches="tight")
        plt.close()
        print(f"✅ IoU heatmap ({metric}) saved: {out}")


# ──────────────────────────────────────────────────────────────────────────────
# 4. Feature-based summary bar chart (best thresholds)
# ──────────────────────────────────────────────────────────────────────────────

def plot_feature_summary(best_path: Path, output_dir: Path):
    best = pd.read_csv(best_path, sep="\t")
    event_types = best["event_type"].tolist()
    x = np.arange(len(event_types))
    w = 0.25

    fig, ax = plt.subplots(figsize=(8, 5))
    ax.bar(x - w, best["precision"], w, label="Precision", color="#1976D2")
    ax.bar(x,     best["recall"],    w, label="Recall",    color="#388E3C")
    ax.bar(x + w, best["f1"],        w, label="F1",        color="#7B1FA2")
    ax.set_xticks(x); ax.set_xticklabels(event_types)
    ax.set_ylim(0, 1.05); ax.set_ylabel("Score")
    ax.set_title("Feature-based evaluation — best thresholds per event type", fontweight="bold")
    ax.legend(); ax.grid(True, alpha=0.3, axis="y")

    # Annotate with threshold values
    for i, row in enumerate(best.itertuples(index=False)):
        ax.text(i, -0.08, f"p={row.prob_threshold:.2f}\nIoU={row.iou_threshold:.2f}",
                ha="center", fontsize=8, transform=ax.get_xaxis_transform())

    plt.tight_layout()
    out = output_dir / "feature_based_summary.png"
    plt.savefig(out, bbox_inches="tight")
    plt.close()
    print(f"✅ Feature summary saved: {out}")

    print("\n📊 Feature-based best thresholds:")
    print(best[["event_type", "prob_threshold", "iou_threshold",
                "precision", "recall", "f1",
                "true_positives", "false_positives", "false_negatives"]].to_string(index=False))


# ──────────────────────────────────────────────────────────────────────────────
# Main
# ──────────────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--config",  required=True)
    parser.add_argument("--model",   required=True)
    parser.add_argument("--history", required=True)
    parser.add_argument("--split",   default="val")
    parser.add_argument("--output",  default=None)
    args = parser.parse_args()

    with open(args.config, "r") as fh:
        config = yaml.safe_load(fh)

    results_dir = Path(config["output"]["results_dir"])
    eval_dir    = results_dir / f"evaluation_{args.split}"
    output_dir  = Path(args.output) if args.output else results_dir / "plots"
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"\n{'='*60}")
    print(f"CODEX EVALUATION PLOTS")
    print(f"{'='*60}")
    print(f"  Model:   {args.model}")
    print(f"  Split:   {args.split}")
    print(f"  Plots →  {output_dir}")

    # ── Step 0: run evaluate_weak5_codex.py if outputs missing ────────────────
    predictions_path = eval_dir / "predictions.tsv"
    sweep_path       = eval_dir / "threshold_sweep.tsv"
    best_path        = eval_dir / "best_thresholds.tsv"

    if not predictions_path.exists() or not sweep_path.exists():
        print(f"\n⚙️  Running evaluate_weak5_codex.py (split={args.split})...")
        python = sys.executable
        cmd = [
            python,
            str(Path(__file__).parent / "evaluate_weak5_codex.py"),
            "--config", args.config,
            "--model",  args.model,
            "--split",  args.split,
        ]
        env = {**__import__("os").environ, "CUDA_VISIBLE_DEVICES": ""}
        result = subprocess.run(cmd, cwd=str(ROOT), env=env)
        if result.returncode != 0:
            print("❌  evaluate_weak5_codex.py failed — aborting.")
            sys.exit(1)
    else:
        print(f"\n♻️  Reusing existing predictions: {eval_dir}")

    # ── Step 1: Training history ───────────────────────────────────────────────
    print(f"\n{'─'*60}")
    print("1/4  Training history")
    history_df = pd.read_csv(args.history, sep="\t")
    best_epoch = int(history_df["val_loss"].idxmin()) + 1
    print(f"     Best epoch: {best_epoch}  (val_loss={history_df['val_loss'].min():.5f},  "
          f"val_f1_macro={history_df.loc[history_df['val_loss'].idxmin(), 'val_masked_f1_macro']:.4f})")
    plot_training_history(Path(args.history), output_dir, best_epoch=best_epoch)

    # ── Step 2: Window-based confusion matrix ─────────────────────────────────
    print(f"\n{'─'*60}")
    print("2/4  Window-based confusion matrix")
    plot_confusion_matrix(predictions_path, output_dir, config=config)

    # ── Step 3: IoU heatmaps ──────────────────────────────────────────────────
    print(f"\n{'─'*60}")
    print("3/4  Feature-based IoU heatmaps")
    plot_iou_heatmaps(sweep_path, output_dir)

    # ── Step 4: Feature summary ───────────────────────────────────────────────
    print(f"\n{'─'*60}")
    print("4/4  Feature-based summary")
    plot_feature_summary(best_path, output_dir)

    print(f"\n✅ All plots written to: {output_dir}")


if __name__ == "__main__":
    main()
