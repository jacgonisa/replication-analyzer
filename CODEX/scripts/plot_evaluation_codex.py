#!/usr/bin/env python
"""
Comprehensive evaluation plots for a CODEX weak-supervision run.

Produces:
  1. Training history  (loss, accuracy, macro-F1, learning rate)
  2. Window-based confusion matrix  (raw + normalised, per-class P/R/F1)
  3. Feature-based IoU heatmaps     (precision / recall / F1 vs prob × IoU threshold)
  4. Feature-based summary table    (best thresholds per event type)

Usage
-----
    python CODEX/scripts/plot_evaluation_codex.py \
        --config  CODEX/configs/weak5_rectangular.yaml \
        --model   CODEX/models/weak4_rectangular_no_ter.keras \
        --history CODEX/results/weak4_rectangular_no_ter/training_history.tsv \
        [--split  val]          # default: val
        [--output CODEX/results/weak4_rectangular_no_ter/plots]

If predictions.tsv and threshold_sweep.tsv already exist under
<results_dir>/evaluation_<split>/ they are reused; otherwise they are
generated by calling evaluate_weak5_codex.py first.
"""

